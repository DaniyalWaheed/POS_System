    public void Backup(ActionEvent event) throws IOException {
        File destination = new File("D:\\BackupDB");
        File source = new File("C:\\Program Files (x86)\\POS\\posDb.accdb");

        // Tests whether the directory denoted by this abstract pathname exists.
        boolean exists = destination.exists();

        if (exists) {
            destination.delete();
            destination.mkdir();
            FileUtils.copyFileToDirectory(source, destination);
        } else {
            destination.mkdir();
            FileUtils.copyFileToDirectory(source, destination);

        }
    }

